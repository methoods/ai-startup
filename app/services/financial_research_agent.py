from app.services.country_filter import CountryFilter


class FinancialResearchAgent:

    def __init__(self):
        self.country_filter = CountryFilter()

    def build_queries(
        self,
        goal: str,
    ) -> list[str]:

        country = self.country_filter.detect(goal)

        if country == "belarus":

            return [

                # Локация
                "аренда торгового помещения Барановичи",
                "аренда магазина Барановичи",

                # Зарплаты
                "зарплата продавца Беларусь",
                "зарплата продавца разливного пива Беларусь",

                # Оборудование
                "холодильная витрина Беларусь купить",
                "холодильная камера Беларусь купить",
                "оборудование для магазина разливного пива Беларусь",
                "кассовое оборудование Беларусь",

                # Регистрация
                "регистрация ИП Беларусь",
                "госпошлина ИП Беларусь",
                "налоги ИП Беларусь торговля",

                # Отраслевые данные
                f"{goal} бизнес-план",
                f"{goal} финансовая модель",
                f"{goal} средний чек",
                f"{goal} расходы",
            ]

        if country == "russia":

            return [
                "аренда торгового помещения",
                "зарплата продавца",
                "холодильная витрина купить",
                "кассовое оборудование",
                "регистрация ИП",
                "налоги ИП торговля",
                f"{goal} бизнес-план",
            ]

        if country == "kazakhstan":

            return [
                "аренда торгового помещения Казахстан",
                "зарплата продавца Казахстан",
                "холодильная витрина Казахстан",
                "регистрация ИП Казахстан",
                f"{goal} бизнес-план",
            ]

        return [
            f"{goal} бизнес-план",
        ]
