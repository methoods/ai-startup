class Planner:

    def create_plan(self, goal: str) -> list[str]:

        goal = goal.lower()

        if any(word in goal for word in [
            "открыть",
            "запустить",
            "создать",
            "бизнес",
            "магазин",
            "кафе",
            "кофейню",
            "детейлинг",
            "салон"
        ]):
            return [
                "Исследование рынка",
                "Анализ конкурентов",
                "Финансовая модель",
                "Маркетинговая стратегия",
                "План запуска"
            ]

        return [
            "Анализ задачи",
            "Подготовка решения"
        ]